<div><strong>Question 3 :</strong> How can I apply online for scholarship?<br>
<strong>Answer :</strong> In order to apply online, please visit the website through URL
www.scholarships.gov.in
</div>
<div><strong>Question 4 :</strong> How to submit the online application?<br> Should I need the user id and password
to apply for scholarship?<br>
<strong>Answer :</strong> Applying procedures for Scholarship Schemes for both Fresh and Renewal are
given below:
Fresh:Student have to Click on the option “Student Login”, on the home page of National
Scholarships Portal. Fill up the application as per the instructions given by the system then click
on save button. After saving, student will get a “Temporary ID”. The system will instruct the
applicant to submit his/her Temporary ID and date of birth to fill subsequent details. Once
registration is complete on click of submit button, a Permanent Registration ID is generated
which can be used for Renewal and tracking the status of application
Renewal:</ Renewal Students have to apply with their Application Id and Date of Birth which they
registered previous year. Student can also use Forgot Application ID to retrieve their ID. Only
those students would be able to Renew who had actually got the scholarships payments last
year from NSP 1.0.
</div>
<div><strong>Question 5 :</strong> Can I edit the information already saved and up-to what time?<br>
<strong>Answer :</strong> All the information can be edited till the closure of application form. After final
submission, your application will be forwarded to the next level and application hereby cannot
be edited.
</div>
<div><strong>Question 6 :</strong> Which fields in the application form are mandatory ?<br>
<strong>Answer:</strong> Fields provided with red asterisk (*) mark are mandatory fields.
</div>
<div><strong>Question 7 :</strong> What happens, if I detect mistakes after forwarding the applications to the next
level?<br>
<strong>Answer :</strong> You should separately inform the mistakes detected by you to the
Institute/District/Region/State. The software provides facility at the level of the Institute & State
to edit & correct limited information.
</div>
<div><strong>Question 8 :</strong> Which fields Institute/State can edit ?<br>
<strong>Answer :</strong> The Fields which can be edited are :</strong> Gender,Religion,Category,Profession,Annual
Income,Aadhar Number,Disability,Day Scholar/Hostlar,Mode of Study,IFSC Code,Account
No.,Admission Fees and Tution Fees.However, corrections made by the Institute/State, if any,
would be conveyed instantly to the student through SMS/email.
</div>
<div><strong>Question 9 :</strong> Do I have to fill up the online application in one sitting ?<br>
<strong>Answer :</strong> No. You can fill up the online application in as many sittings as you wish, until
you are satisfied that you have entered all desirable fields correctly. The software provides
facility to save your application at every stage.
</div>
<div><strong>Question 10 :</strong> What is UID number/Aadhaar Number?<br>
<strong>Answer :</strong> UID number otherwise known as ‘Aadhaar’ number is Unique Identification
Number given by Unique Identification Authority of India (UIDAI). Aadhaar is unique 12 digit
number assigned after de-duplication of biometrics.
</div>
<div><strong>Question 11 :</strong> Do I need to get my Aadhaar Card to apply for Scholarship ?<br>
<strong>Answer :</strong> Aadhaar No. is not Mandatory for the Students in order to Register and fill up
the application form online. Students can apply for Scholarship without entering the Aadhaar no.
but in that case they have to enter Aadhaar Enrollment Id. For the States of Aasam, Meghalya
and Mizoram Aadhar Number is not mandatory.
</div>
<div><strong>Question 12 :</strong></div> Is there any permanent ID? How will it be communicated to me ?<br>
<strong>Answer :</strong></div> Yes. An Application ID (Permanent ID) will be provided to the candidate once
his/her Registration is done. It will be conveyed to candidates through SMS and e-mail. Students
should memorize their Application ID as it will be required while applying for Free. No, you cannot apply as a fresh if you are a Renewal candidate. Your applicationwill be rejected in that case.<br>
</div>
<div><strong>Question 14 :</strong> What should I do, if I do not find my institute name in the drop-down menu ?<br>
